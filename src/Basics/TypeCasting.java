package Basics;

public class TypeCasting {

	public static void main(String[] args) {

		//Widening Casting

		int myint = 10;

		double mydouble = myint;

		System.out.println(mydouble);

		//Narrowing Casting
		
		double mydouble2 = 22.1;
		
		int myint2 = (int) mydouble2;
		
		System.out.println(myint2);



	}

}
